public class EqSearch {
	private int distance;
	private int minmag;

	public EqSearch(){
	}

	public int getDistance(){
		return distance;
	}

	public int getMinmag(){
		return minmag;
	}

	public void setDistance(int dist){
		this.distance = dist;
	}

	public void setMinmag(int min){
		this.minmag = min;
	}
}



